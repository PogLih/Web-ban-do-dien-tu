package model;

/**
 *
 * @author Linh
 */
public class Product {
    private String code;
    private String name;
    private float price;
    private String category;
    private String img;

    public Product(String code, String name, float price,String category, String img) {
        this.code = code;
        this.name = name;
        this.price = price;
        this.category =category;
        this.img = img;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public Product() {
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
    
}
